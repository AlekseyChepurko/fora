<?php

namespace Fora\TestBundle\Entity;

/**
 * Question
 */
class Question
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $type;

    /**
     * @var string
     */
    public $body;

    /**
     * @var array
     */
    private $answers;

    /**
     * @var array
     */
    private $variants;

    /**
     * @var string
     */
    private $picUrl;

    /**
     * @var string
     */
    private $testName;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set type
     *
     * @param string $type
     *
     * @return Question
     */
    public function setType($type)
    {
        $this->type = $type;

        return $this;
    }

    /**
     * Get type
     *
     * @return string
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * Set body
     *
     * @param string $body
     *
     * @return Question
     */
    public function setBody($body)
    {
        $this->body = $body;

        return $this;
    }

    /**
     * Get body
     *
     * @return string
     */
    public function getBody()
    {
        return $this->body;
    }

    /**
     * Set answers
     *
     * @param array $answers
     *
     * @return Question
     */
    public function setAnswers($answers)
    {
        $this->answers = $answers;

        return $this;
    }

    /**
     * Get answers
     *
     * @return array
     */
    public function getAnswers()
    {
        return $this->answers;
    }

    /**
     * Set variants
     *
     * @param array $variants
     *
     * @return Question
     */
    public function setVariants($variants)
    {
        $this->variants = $variants;

        return $this;
    }

    /**
     * Get variants
     *
     * @return array
     */
    public function getVariants()
    {
        return $this->variants;
    }

    /**
     * Set picUrl
     *
     * @param string $picUrl
     *
     * @return Question
     */
    public function setPicUrl($picUrl)
    {
        $this->picUrl = $picUrl;

        return $this;
    }

    /**
     * Get picUrl
     *
     * @return string
     */
    public function getPicUrl()
    {
        return $this->picUrl;
    }

    /**
     * Set testName
     *
     * @param string $testName
     *
     * @return Question
     */
    public function setTestName($testName)
    {
        $this->testName = $testName;

        return $this;
    }

    /**
     * Get testName
     *
     * @return string
     */
    public function getTestName()
    {
        return $this->testName;
    }
    /**
     * @var \Fora\TestBundle\Entity\Test
     */
    private $test;


    /**
     * Set test
     *
     * @param \Fora\TestBundle\Entity\Test $test
     *
     * @return Question
     */
    public function setTest(\Fora\TestBundle\Entity\Test $test = null)
    {
        $this->test = $test;

        return $this;
    }

    /**
     * Get test
     *
     * @return \Fora\TestBundle\Entity\Test
     */
    public function getTest()
    {
        return $this->test;
    }
}
